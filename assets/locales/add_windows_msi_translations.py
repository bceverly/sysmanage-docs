    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Navigate to agent_installation.installation and add windows_msi translations
    if 'agent_installation' not in data:
        print(f"Warning: agent_installation not found in {lang_code}.json")
        return

    if 'installation' not in data['agent_installation']:
        print(f"Warning: installation section not found in {lang_code}.json")
        return

    # Add windows_msi translations
    data['agent_installation']['installation']['windows_msi'] = translations

    # Also add tab reference
    if 'windows' not in data['agent_installation']['installation']:
        data['agent_installation']['installation']['windows'] = {}
