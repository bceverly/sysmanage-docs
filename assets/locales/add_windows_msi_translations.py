def add_translations_to_file(lang_code, translations):
    """Add Windows MSI translations to a specific language file."""
    locale_dir = Path(__file__).parent
    file_path = locale_dir / f"{lang_code}.json"

    # Read existing translations
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Navigate to agent_installation.installation and add windows_msi translations
    if 'agent_installation' not in data:
        print(f"Warning: agent_installation not found in {lang_code}.json")
        return

    if 'installation' not in data['agent_installation']:
        print(f"Warning: installation section not found in {lang_code}.json")
        return

    # Add windows_msi translations
    data['agent_installation']['installation']['windows_msi'] = translations

    # Also add tab reference
    if 'windows' not in data['agent_installation']['installation']:
        data['agent_installation']['installation']['windows'] = {}
    data['agent_installation']['installation']['windows']['tab'] = "Windows" if lang_code == 'en' else "Windows"

    # Write back with proper formatting
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"[OK] Updated {lang_code}.json")

