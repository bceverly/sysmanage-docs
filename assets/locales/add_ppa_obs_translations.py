def add_translations_to_locale(locale_code, locale_file):
    """Add new translations to a locale file."""
    print(f"Processing {locale_code}...")

    # Load existing translations
    with open(locale_file, 'r', encoding='utf-8') as f:
        data = json.load(f)

    # Ensure agent_installation.installation.apt exists
    if 'agent_installation' not in data:
        data['agent_installation'] = {}
    if 'installation' not in data['agent_installation']:
        data['agent_installation']['installation'] = {}
    if 'apt' not in data['agent_installation']['installation']:
        data['agent_installation']['installation']['apt'] = {}
    if 'zypper_opensuse' not in data['agent_installation']['installation']:
        data['agent_installation']['installation']['zypper_opensuse'] = {}
    if 'zypper_sles' not in data['agent_installation']['installation']:
        data['agent_installation']['installation']['zypper_sles'] = {}

    # Get translations for this locale
    trans = TRANSLATIONS.get(locale_code, TRANSLATIONS['en'])

    # Add PPA translations to APT section
    apt_section = data['agent_installation']['installation']['apt']
    for key in ['ppa_intro', 'ppa_title', 'ppa_supported', 'ppa_22_04', 'ppa_24_04',
                'ppa_24_10', 'ppa_25_04', 'ppa_25_10', 'ppa_benefits_title',
                'ppa_benefits_official', 'ppa_benefits_official_desc', 'ppa_benefits_multi',
                'ppa_benefits_multi_desc', 'ppa_benefits_trusted', 'ppa_benefits_trusted_desc',
                'ppa_quick_start', 'ppa_note']:
        apt_section[key] = trans[key]

    # Add OBS translations to openSUSE zypper section
    obs_section = data['agent_installation']['installation']['zypper_opensuse']
    for key in ['obs_intro', 'obs_title', 'obs_supported', 'obs_tumbleweed', 'obs_leap_15_6',
                'obs_leap_15_5', 'obs_sle_15_sp6', 'obs_sle_15_sp5', 'obs_benefits_title',
                'obs_benefits_official', 'obs_benefits_official_desc', 'obs_benefits_multi',
                'obs_benefits_multi_desc', 'obs_benefits_integration', 'obs_benefits_integration_desc',
                'obs_quick_start_leap', 'obs_quick_start_tumbleweed', 'obs_quick_start_sles',
                'obs_note', 'obs_view_builds']:
        obs_section[key] = trans[key]

    # Also add OBS translations to SLES zypper section
    sles_section = data['agent_installation']['installation']['zypper_sles']
    for key in ['obs_intro', 'obs_title', 'obs_supported', 'obs_tumbleweed', 'obs_leap_15_6',
                'obs_leap_15_5', 'obs_sle_15_sp6', 'obs_sle_15_sp5', 'obs_benefits_title',
                'obs_benefits_official', 'obs_benefits_official_desc', 'obs_benefits_multi',
                'obs_benefits_multi_desc', 'obs_benefits_integration', 'obs_benefits_integration_desc',
                'obs_quick_start_leap', 'obs_quick_start_tumbleweed', 'obs_quick_start_sles',
                'obs_note', 'obs_view_builds']:
        sles_section[key] = trans[key]

    # Save updated translations
    with open(locale_file, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"  ‚úì Added {len(trans)} keys to {locale_code}")

def main():
    """Main function to process all locale files."""
    script_dir = Path(__file__).parent

    # Process all locale files
    for locale_code in TRANSLATIONS.keys():
        locale_file = script_dir / f"{locale_code}.json"
        if locale_file.exists():
            add_translations_to_locale(locale_code, locale_file)
        else:
            print(f"WARNING: Locale file not found: {locale_file}")

    print("\n‚úì All translations added successfully!")
    print("\nNext steps:")
    print("1. Update installation.html to add the new PPA and OBS sections")
    print("2. Test the changes on the documentation site")

if __name__ == '__main__':
    main()
